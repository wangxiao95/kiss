<template>
    <div class="hello">
        <img class="indexImg" src="../assets/imgs/timg.jpeg" alt="">
        <button class="skip" @click="skip">跳过{{count}}</button>
    </div>
</template>

<script>

  export default {
    name: 'HelloWorld',
    data() {
      return {
        count: 3
      }
    },
    components: {

    },
    computed: {},
    methods: {
      skip() {
        this.$router.push('/home')
      }
    },
    mounted() {
      let timer = setInterval(() => {
        if (this.count == 0) {
          clearInterval(timer)
          timer = null
          this.$router.push('/home')
          return
        }
        this.count--
      }, 1000)
    }
  }
</script>

